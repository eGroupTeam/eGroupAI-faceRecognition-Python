class FrameFace:
    def __init__(self):
        self.frameFaceNo = ""
        self.frameFacePath = ""

    def getFrameFaceNo(self):
        return self.frameFaceNo

    def setFrameFaceNo(self, frameFaceNo: str):
        self.frameFaceNo = frameFaceNo

    def getFrameFacePath(self):
        return self.frameFacePath

    def setFrameFacePath(self, frameFacePath: str):
        self.frameFacePath = frameFacePath
