class StartupInfo:
	def __init__(self):
		self.item=None
		self.status=None
		self.time=None

	def getItem(self):     
		return self.item   

	def setItem(self,item):
		self.item=item     
		
	def getStatus(self):       
		return self.status     

	def setStatus(self,status):
		self.status=status     
		
	def getTime(self):
		return self.time

	def setTime(self,time):
		self.time=time		
